package chapter1to5;

public class IntegerTest {
    public static void main(String[] args) {
        int i1 = 10;
        int i2 = 20;
        System.out.println("i1 : "+ i1);
        System.out.println("i2 : "+ i2);
        i2 = i1;
        System.out.println("i1 : "+ i1);
        System.out.println("i2 : "+ i2);
        i1 = 100;
        System.out.println("i1 : "+ i1);
        System.out.println("i2 : "+ i2);
    }
}
